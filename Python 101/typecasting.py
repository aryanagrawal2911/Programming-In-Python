# Type Casting is used to change a data from one data type to another
heightcm = input("Enter height in cm : ")
height = float(heightcm)
print("Height in feet : ", height / 30.48)

height = float(input("Enter height in cm : "))
print("Height in feet : ", height / 30.48)
