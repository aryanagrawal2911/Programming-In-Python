age = 37
print(age)

newage = age + 69
print(newage)

age = age + 7
# "age += 7" is the same as "age = age + 7"
# same goes for "-" , "*" , "/", "%"
print(age)

# Strings are concatenated as they are, without any gaps between them
# "+" is used for the concatenation
# "*" prints the string the mentioned number of times in the same line without any gaps
# "*" begets error on using Floats
# "*" with ZERO or Negatives yields only a BLANK line
print("matar" + "chol")
text = "matar" + "chol"
print(text)
print(text * 9)
print(text * -2)
