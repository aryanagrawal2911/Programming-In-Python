# "Underscores" (or "_") are the only special characters usable in a number
# and the do not change the value anyway whatsoever
1327846
1_327_846

# scientific notation of some numbers can be used
# 3e4 = 3E4 = 3 * 10000 , similarly for 3e-4 = 3E-4 = 3 * 1/1000
# print(0.000000000000000000000003) yields 3e-24
print(0.000000000000000000000003)

# octal representation
# starts with "0O" or "0o"
print(0o123)

# hexadecimal representation
# starts with "0X" or "0x"
print(0x123)
